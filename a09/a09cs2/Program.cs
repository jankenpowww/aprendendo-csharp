//Binários em C# - atribuímos um valor binário iniciando com o prefixo '0b' seguido
//da representação binária de um valor. O valor à ser atribuído é o valor decimal inteiro
//desse valor.

//Atribuindo o valor 25 em binário.
var bin25 = 0b11001;

//Atribuindo o valor 50 em binário.
var bin50 = 0b110010;

//Atribuindo o valor 100 em binário.
var bin100 = 0b1100100;

Console.WriteLine(bin25);
Console.WriteLine(bin50);
Console.WriteLine(bin100);